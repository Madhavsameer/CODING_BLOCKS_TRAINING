public class looping {
    public static void main(String[] args) {
        //for loop

        for(int i=1; i<=5;  i++){
            System.out.println("My name is sameer");
        }

        //while loop
        int j=1;
        while(j<6){
            System.out.println(j);
            j++;
        }

        //do while loop
        int k=1;
        do{
            System.out.println(k);
            k++;
        }
        while(k<7);




    }
    
}
