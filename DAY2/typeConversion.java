public class typeConversion {
    public static void main(String[] args) {

        //Automatic Type Conversion
        int num1=5;
        float num2=num1;
        System.out.println(num2);

        //Manual type Conversion
        int num3=7;
        float num4=(float) num3;
        System.out.println(num4);
    }
    
}
